private void resetMenuColorsExcept(javax.swing.JLabel selectedLabel) {
    javax.swing.JLabel[] menuLabels = {
        lb_home, lb_hoadon, lb_qlnhanvien,
        lb_chitiethoadon, lb_qlsanpham,
        lb_quanlykhachhang, lb_thongke, lb_qlkhachhang, lb_dangxuat
    };

    for (javax.swing.JLabel label : menuLabels) {
        if (label != selectedLabel) {
            label.setBackground(null);
            label.setOpaque(false);
        }
    }
}

