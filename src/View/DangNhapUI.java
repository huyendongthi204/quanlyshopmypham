    private void btn_dangnhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_dangnhapActionPerformed
       
        String username = txt_tendangnhap.getText();
        String password = String.valueOf(pass.getPassword());
        String selectedRole = cb_Role.getSelectedItem().toString();

        if (accounts.containsKey(username)) {
        String[] info = accounts.get(username);
        String pass = info[0];
        String role = info[1];
    
    if (password.equals(pass) && role.equals(selectedRole)) {
        if (role.equals("Admin")) {
            try {
                new  FromAdmin().setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(DangNhapUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            new FromNhanVien().setVisible(true);
        }
        dispose(); // đóng form đăng nhập
    } else {
        JOptionPane.showMessageDialog(this, "Sai mật khẩu hoặc sai quyền!");
    }
} else {
    JOptionPane.showMessageDialog(this, "Tài khoản không tồn tại!");
}
      

           if (check_ghinhodangnhap.isSelected()) {
    try (FileWriter fw = new FileWriter("remember.txt")) {
        fw.write(username + "\n" + password);
    } catch (IOException ex) {
        ex.printStackTrace();
    }
} else {
    // Nếu không chọn thì xóa file (nếu có)
    File f = new File("remember.txt");
    if (f.exists()) f.delete();
}



    }//GEN-LAST:event_btn_dangnhapActionPerformed

