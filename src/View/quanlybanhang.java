    private void suaHoaDon() {
    int selectedRow = tbhoadon.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn h√≥a ƒë∆°n c·∫ßn s·ª≠a!");
        return;
    }

    String oldMaHD = tbhoadon.getValueAt(selectedRow, 0).toString(); // M√£ c≈©
    String newMaHD = txtMaHD.getText().trim();
  String maKH = txtkhachhang.getText();

if (maKH == null || maKH.trim().isEmpty() || 
    maKH.trim().equals("M√£ KH") || txtkhachhang.getForeground().equals(Color.GRAY)) {
    maKH = null;
} else {
    maKH = maKH.trim();
}



    String tongTienStr = txtTongTien.getText().trim();

    if (newMaHD.isEmpty()) {
        JOptionPane.showMessageDialog(this, "M√£ h√≥a ƒë∆°n kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng!");
        return;
    }

    if (NgayLap.getDate() == null) {
        JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn ng√†y l·∫≠p!");
        return;
    }

    Date ngay = NgayLap.getDate();
    java.sql.Date sqlDate = new java.sql.Date(ngay.getTime());

    BigDecimal tongTien;
    try {
        tongTien = new BigDecimal(tongTienStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "T·ªïng ti·ªÅn kh√¥ng h·ª£p l·ªá!");
        return;
    }

    // N·∫øu s·ª≠a m√£ h√≥a ƒë∆°n => c·∫ßn c·∫≠p nh·∫≠t ·ªü c·∫£ 2 b·∫£ng
    if (!newMaHD.equals(oldMaHD)) {
        // Ki·ªÉm tra m√£ m·ªõi c√≥ b·ªã tr√πng kh√¥ng
        if(hdDao.getHoaDonByMa(newMaHD) != null) {
            JOptionPane.showMessageDialog(this, "M√£ h√≥a ƒë∆°n m·ªõi ƒë√£ t·ªìn t·∫°i!");
            return;
        }

        // C·∫≠p nh·∫≠t trong b·∫£ng chi ti·∫øt h√≥a ƒë∆°n tr∆∞·ªõc
        ctDao.updateMaHDCuaChiTiet(oldMaHD, newMaHD);

        // C·∫≠p nh·∫≠t trong b·∫£ng h√≥a ƒë∆°n
        hdDao.updateMaHD(oldMaHD, newMaHD);
    }

    // C·∫≠p nh·∫≠t c√°c th√¥ng tin kh√°c
   HoaDon hd = new HoaDon(newMaHD, sqlDate, maKH, tongTien);
hdDao.updateHoaDon(hd, maKH);

    JOptionPane.showMessageDialog(this, "S·ª≠a h√≥a ƒë∆°n th√†nh c√¥ng!");
    loadTableHoaDon();
}
